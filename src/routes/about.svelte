          <div class="mx-auto lg:mx-0 w-4/5 pt-3 border-b-2 border-teal-500 opacity-25" style="margin-bottom: 15px;"></div>
          <p class="mb-2">Cette ambition est née à Gruffy, en Haute-Savoie, dans le GAEC La Ferme du Parquet.</p>
          <p class="mb-2">Benoît, le frère des deux co-fondateurs Noël et Geoffrey et le gérant du GAEC,
            explique qu’il a besoin d’un outil pour gérer les commandes de yaourt que les magasins
            bio font toutes les semaines.</p>
          <p class="mb-2">Deux ans plus tard, Noël se demande si d’autres producteurs pourraient être intéressés par ce genre d’outil.
            Mais tous les producteurs ne font pas de la vente en magasin et certains préfèrent vendre directement à leur ferme ou sur les marchés.</p>

            <p class="mb-2 text-primary">« Ça donnerait quoi si on faisait une plateforme qui permette à la fois à un commerçant
            et à un consommateur d’acheter des produits locaux, tout en assurant au producteur un gain 
            de temps significatif et de la flexibilité dans ses ventes ? »</p>

            <p class="mb-2">La réponse, c’est une <b class="text-primary">fraise</b>. Une fraise et des milliers de lignes de code.</p>
