          <h1 class="text-3xl font-bold pt-8 lg:pt-0">Pourquoi nous ?</h1>
          <div class="mx-auto lg:mx-0 w-4/5 pt-3 border-b-2 border-teal-500 opacity-25" style="margin-bottom: 15px;"></div>
           <p class="mb-2">Les problèmes des producteurs, Noël et Geoffrey les connaissent bien. 
            Petits-fils, neveux et frères de paysans, ils ont à coeur d’aider les producteurs à gagner du temps. 
            C’est pour eux, littéralement, une affaire de famille.</p>

            <p class="mb-2">Si on regarde de loin, on pourrait croire que Sheaft est une start-up. Mais non, pas du tout. 
            Croissance exponentielle, non merci ! Comment aider les producteurs à résoudre leurs problèmes si nous devenons 
            gros au point qu’ils ne deviennent que des chiffres et des adresses pour nous ?</p>

            <p class="mb-2">Nous voulons prouver qu’il est possible de créer de petites entreprises innovantes sans avoir à chercher 
            un « business model scalable et replicable » qui ouvre la voie à une « croissance exponentielle » pour 
            devenir une licorne « disruptive ».</p>
